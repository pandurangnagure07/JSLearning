console.log("Swapping Variables");
var num1 = "sweety";
var num2 = "cutie";
console.log("\n*****Before Swaping*****")
num1=num2;
console.log(num1,num2);

console.log("\n*****After Swaping*****")
var num1 = "sweety";
var num2 = "cutie";
var temp = num1 ;
num1 = num2;
num2 = temp;
